#include <bits/stdc++.h>
using namespace std;
#define nl "\n"
// https://atcoder.jp/contests/abc152/tasks/abc152_a

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m;
    cin >> n >> m;

    if (n == m)
        cout << "Yes" << nl;
    else
        cout << "No" << nl;

    return 0;
}
