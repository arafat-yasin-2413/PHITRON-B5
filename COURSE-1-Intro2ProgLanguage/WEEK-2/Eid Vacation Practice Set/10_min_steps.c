#include<stdio.h>

/*
ধরুন ৯ ইনপুট করলেন এখানে আপনি ৩+৩+৩ করে গেলেই হচ্ছে আবার ১০ হলে সমস্যা হয় যে ৩+৩+৩+১ করতে হয়। মানে দেখুন যদি ইনপুটটা ৩ দ্বারা বিভাজ্য হয় তাহলে তাকে ৩ দিয়ে ভাগ করে দিলেই রেজাল্ট চলে আসে আবার ৩ দ্বারা বিভাজ্য না হলে তাকে ৩ দ্বারা ভাগ করে ১ যোগ করলেই রেজাল্ট আসে। এভাবে করে দেখুন
*/
int main()
{
    int n;
    scanf("%d",&n);

    if (n%3 == 0)
    {
        printf("%d\n",n/3);

    }

    else
    {
        printf("%d\n",(n/3)+1);
    }
    
    return 0;
}